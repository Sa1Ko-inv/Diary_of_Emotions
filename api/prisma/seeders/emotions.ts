export async function seedEmotion(prisma: PrismaClient) {
  for (const group of emotionData) {
    const createdGroup = await prisma.emotionGroup.create({
      data: {
        name: group.name,
        color: group.color,
        types: {
          create: group.types.map((name) => ({ name })),
        },
      },
    });
    console.log(`Создана группа: ${createdGroup.name}`);
  }
}
